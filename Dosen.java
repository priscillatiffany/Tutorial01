
public class Dosen {
	private String nama, nip;

	public Dosen(String nama, String nip) {
		super();
		this.nama = nama;
		this.nip = nip;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getNip() {
		return nip;
	}

	public void setNip(String nip) {
		this.nip = nip;
	}

}
